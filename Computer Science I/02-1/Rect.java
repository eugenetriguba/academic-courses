public class Rect
{
	// Computes the area of a rectangle with a height of 3.2 & width of 4.1
	public static void main(String[] args)
	{
		double height = 3.2;
		double width = 4.1;
		double area = height * width;
		
		System.out.println("The area of a rectangle with height " + 
			height + " and width " + width + " is " + area);
	}
}